import unicodedata

POLISH_TO_ENGLISH_MAPPING = {
    'ą': 'a', 'ć': 'c', 'ę': 'e', 'ł': 'l', 'ń': 'n', 'ó': 'o', 'ś': 's', 'ź': 'z', 'ż': 'z',
    'Ą': 'A', 'Ć': 'C', 'Ę': 'E', 'Ł': 'L', 'Ń': 'N', 'Ó': 'O', 'Ś': 'S', 'Ź': 'Z', 'Ż': 'Z',
}

def replace_polish_characters(text):
    return ''.join([POLISH_TO_ENGLISH_MAPPING.get(char, char) for char in text])
